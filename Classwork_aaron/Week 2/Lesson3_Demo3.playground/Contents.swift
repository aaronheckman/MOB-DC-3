let num = -5

if num > 0 {
    println("Yes, \(num) is greater than zero")
} else {
    println("No, \(num) is not greater than zero")
    
}


let thing = 0
if thing > 0{
    println("Yes, \(thing) is greater than zero")
} else if thing == 0 {
    println("\(thing) is in fact zero")
} else {
    println("](thing) is less than zero")
}

var guy = 5

